package com.arahant.services.standard.crm.salesActivity;

import com.arahant.services.TransmitInputBase;
import com.arahant.annotation.Validation;


/**
 * 
 *
 * Created on Feb 8, 2007
 *
 */
public class NewActivityInput extends TransmitInputBase {

	@Validation (type="date", required=false)
	private int lastActiveDate;
	@Validation (required=false)
	private short points;
	@Validation (required=false)
	private String description;
	@Validation (required=true)
	private String code;
	@Validation (required=false)
	private boolean allCompanies;
	

	public int getLastActiveDate()
	{
		return lastActiveDate;
	}
	public void setLastActiveDate(int lastActiveDate)
	{
		this.lastActiveDate=lastActiveDate;
	}
	public short getPoints()
	{
		return points;
	}
	public void setPoints(short points)
	{
		this.points=points;
	}
	public String getDescription()
	{
		return description;
	}
	public void setDescription(String description)
	{
		this.description=description;
	}
	public String getCode()
	{
		return code;
	}
	public void setCode(String code)
	{
		this.code=code;
	}

	public boolean getAllCompanies() {
		return allCompanies;
	}

	public void setAllCompanies(boolean allCompanies) {
		this.allCompanies = allCompanies;
	}

}

	
