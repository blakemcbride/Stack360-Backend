package com.arahant.services.standard.hr.benefitAuditExport;

import com.arahant.business.BHRBenefit;


public class ListBenefitsReturnItem {

	private String categoryName;
	private String categoryId;
	private String benefitName;
	private String benefitId;
	private String configName;
	private String configId;

	public String getConfigId() {
		return configId;
	}

	public void setConfigId(String configId) {
		this.configId = configId;
	}

	public String getConfigName() {
		return configName;
	}

	public void setConfigName(String configName) {
		this.configName = configName;
	}

	public String getBenefitId() {
		return benefitId;
	}

	public void setBenefitId(String benefitId) {
		this.benefitId = benefitId;
	}

	public String getBenefitName() {
		return benefitName;
	}

	public void setBenefitName(String benefitName) {
		this.benefitName = benefitName;
	}

	public String getCategoryId() {
		return categoryId;
	}

	public void setCategoryId(String categoryId) {
		this.categoryId = categoryId;
	}

	public String getCategoryName() {
		return categoryName;
	}

	public void setCategoryName(String categoryName) {
		this.categoryName = categoryName;
	}



	
	public ListBenefitsReturnItem() {
		
	}

	ListBenefitsReturnItem(BHRBenefit bc) {

		categoryId = bc.getCategoryId();
		categoryName = bc.getCategoryName();
		benefitId = bc.getBenefitId();
		benefitName = bc.getName();
		configId = "";
		configName = "(All)";

	}
	
}

	
