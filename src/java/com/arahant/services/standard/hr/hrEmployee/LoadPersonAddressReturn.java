package com.arahant.services.standard.hr.hrEmployee;

import com.arahant.business.BPerson;
import com.arahant.services.TransmitReturnBase;

public class LoadPersonAddressReturn extends TransmitReturnBase {

	public LoadPersonAddressReturn() {
	}

	public void setData (BPerson person)
	{
		workPhone = person.getWorkPhoneNumber();
        workFax = person.getWorkFaxNumber();
		homePhone = person.getHomePhone();
        mobilePhone = person.getMobilePhone();
        city = person.getCity();
        stateProvince = person.getState();
        zipPostalCode = person.getZip();
        addressLine1 = person.getStreet();
        addressLine2 = person.getStreet2();
        country = person.getCountry();
        county = person.getCounty();
	}
	
	private String addressLine1;
    private String addressLine2;
	private String homePhone;
    private String mobilePhone;
    private String city;
    private String country;
    private String county;
    private String stateProvince;
    private String zipPostalCode;
	private String workPhone;
    private String workFax;

	public String getAddressLine1() {
		return addressLine1;
	}

	public void setAddressLine1(String addressLine1) {
		this.addressLine1 = addressLine1;
	}

	public String getAddressLine2() {
		return addressLine2;
	}

	public void setAddressLine2(String addressLine2) {
		this.addressLine2 = addressLine2;
	}

	public String getCity() {
		return city;
	}

	public void setCity(String city) {
		this.city = city;
	}

	public String getCountry() {
		return country;
	}

	public void setCountry(String country) {
		this.country = country;
	}

	public String getCounty() {
		return county;
	}

	public void setCounty(String county) {
		this.county = county;
	}

	public String getHomePhone() {
		return homePhone;
	}

	public void setHomePhone(String homePhone) {
		this.homePhone = homePhone;
	}

	public String getMobilePhone() {
		return mobilePhone;
	}

	public void setMobilePhone(String mobilePhone) {
		this.mobilePhone = mobilePhone;
	}

	public String getStateProvince() {
		return stateProvince;
	}

	public void setStateProvince(String stateProvince) {
		this.stateProvince = stateProvince;
	}

	public String getWorkFax() {
		return workFax;
	}

	public void setWorkFax(String workFax) {
		this.workFax = workFax;
	}

	public String getWorkPhone() {
		return workPhone;
	}

	public void setWorkPhone(String workPhone) {
		this.workPhone = workPhone;
	}

	public String getZipPostalCode() {
		return zipPostalCode;
	}

	public void setZipPostalCode(String zipPostalCode) {
		this.zipPostalCode = zipPostalCode;
	}

}

	
